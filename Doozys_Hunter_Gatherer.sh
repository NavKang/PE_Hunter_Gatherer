#!/bin/bash

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
PURPLE='\033[0;35m'
NC='\033[0m'

echo -e "\033[1;31m
    ,---,                                                 
  .'  .' \`\\                                               
,---.'     \\    ,---.     ,---.         ,----,            
|   |  .\`\\  |  '   ,'\\   '   ,'\\      .'   .\`|            
:   : |  '  | /   /   | /   /   |  .'   .'  .'      .--,  
|   ' '  ;  :.   ; ,. :.   ; ,. :,---, '   ./     /_ ./|  
'   | ;  .  |'   | |: :'   | |: :;   | .'  /   , ' , ' :  
|   | :  |  ''   | .; :'   | .; :\`---' /  ;--,/___/ \\: |  
'   : | /  ; |   :    ||   :    |  /  /  / .\`| .  \\  ' |  
|   | '\` ,/   \\   \\  /  \\   \\  / ./__;     .'   \\  ;   :  
;   :  .'      \`----'    \`----'  ;   |  .'       \\  \\  ;  
|   ,.'                          \`---'            :  \\  \\ 
'---'                                              \\  ' ; 
                                                    \`--\`  
\033[0m"
echo "++++++++++++++++++++++++++++++++++++++++++++++++++++++"
echo "Enumeration made easier with the Doozy Hunter Gatherer"
echo "++++++++++++++++++++++++++++++++++++++++++++++++++++++"
echo
echo
# System information
echo -e "${YELLOW}System information:${NC}"
echo "=========================================================================================================================================="
uname -a
cat /etc/issue
cat /etc/*-release
cat /proc/version
echo
echo -e "${YELLOW}Groups:${NC}"
echo "=========================================================================================================================================="
# Groups
groups
id
echo
echo -e "${RED}Writable directories:${NC}"
echo "=========================================================================================================================================="
# Writable directories
find / -perm -2 -type d 2>/dev/null
echo
echo -e "${GREEN}SUID files:${NC}"
echo "=========================================================================================================================================="
# SUID files
find / -perm -u=s -type f 2>/dev/null
echo
echo -e "${RED}World-writable files:${NC}"
echo "=========================================================================================================================================="
# World-writable files
find / -perm -o w -type f 2>/dev/null
echo
echo -e "${BLUE}Passwd file:${NC}"
echo "=========================================================================================================================================="
# Passwd file
cat /etc/passwd
echo
echo -e "${GREEN}Running processes:${NC}"
echo "=========================================================================================================================================="
# Running processes
ps aux
echo
echo -e "${BLUE}Open ports:${NC}"
echo "=========================================================================================================================================="
# Open ports
netstat -tulnp

echo -e "${BLUE}"
